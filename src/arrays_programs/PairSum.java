package arrays_programs;

public class PairSum {

	public static void main(String[] args) {
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		int[] a = { 2, 3, 5, 8, 6, 1, 1 };
		for (int i = 0; i < a.length; i++) {
			for (int j = 0; j < a.length; j++) {
				if (i != j && (a[i] + a[j] == 10)) {
					System.out.println("(" + a[i] + " " + a[j] + ")");
				}
			}
		}

	}
}
